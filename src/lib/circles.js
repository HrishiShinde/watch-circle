import { supabase } from './supabase'

// ─── Shared select for circle members with profile info ───────────────────────
const MEMBER_SELECT = `
  role, joined_at,
  profiles!circle_members_user_id_fkey ( id, display_name )
`

// ─── Shape raw member row → clean member object ───────────────────────────────
export function shapeMember(row) {
  return {
    user_id:      row.profiles?.id,
    display_name: row.profiles?.display_name || 'Someone',
    initials:     (row.profiles?.display_name || 'S').slice(0, 1).toUpperCase(),
    role:         row.role,
    joined_at:    row.joined_at,
  }
}

// ─── Fetch all circles for a user ─────────────────────────────────────────────
export async function fetchCircles(userId) {
  const { data, error } = await supabase
    .from('circle_members')
    .select(`
      role,
      circles (
        id, name, description, is_personal,
        open_invites, created_by, created_at
      )
    `)
    .eq('user_id', userId)

  if (error) throw error

  const all = data.map(row => ({ ...row.circles, role: row.role }))
  return {
    personalCircle: all.find(c => c.is_personal) ?? null,
    circles:        all.filter(c => !c.is_personal),
  }
}

// ─── Fetch members of a circle ────────────────────────────────────────────────
export async function fetchCircleMembers(circleId) {
  const { data, error } = await supabase
    .from('circle_members')
    .select(MEMBER_SELECT)
    .eq('circle_id', circleId)
    .order('joined_at', { ascending: true })

  if (error) throw error
  return data.map(shapeMember)
}

// ─── Create a circle ──────────────────────────────────────────────────────────
// Trigger handle_new_circle() auto-adds creator as moderator
export async function createCircle(circleData, userId) {
  const { data, error } = await supabase
    .from('circles')
    .insert({
      name:         circleData.name.trim(),
      description:  circleData.description?.trim() || null,
      open_invites: circleData.open_invites ?? false,
      created_by:   userId,
      is_personal:  false,
    })
    .select()
    .single()

  if (error) throw error
  return { ...data, role: 'moderator' }
}

// ─── Update a circle (mod only — enforced by RLS) ─────────────────────────────
export async function updateCircle(circleId, circleData) {
  const { data, error } = await supabase
    .from('circles')
    .update({
      name:         circleData.name?.trim(),
      description:  circleData.description?.trim() || null,
      open_invites: circleData.open_invites,
    })
    .eq('id', circleId)
    .select()
    .single()

  if (error) throw error
  return data
}

// ─── Delete a circle (mod only — enforced by RLS) ─────────────────────────────
export async function deleteCircle(circleId) {
  const { error } = await supabase
    .from('circles')
    .delete()
    .eq('id', circleId)

  if (error) throw error
}

