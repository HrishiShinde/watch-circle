// ─── Generate an invite code ──────────────────────────────────────────────────
// code is auto-generated by DB default, we just insert and read it back
export async function generateInviteCode(circleId, userId) {
  const { data, error } = await supabase
    .from('circle_invites')
    .insert({
      circle_id:  circleId,
      invited_by: userId,
    })
    .select('code')
    .single()

  if (error) throw error
  return data.code
}

// ─── Fetch existing active invite codes for a circle ─────────────────────────
export async function fetchInviteCodes(circleId) {
  const { data, error } = await supabase
    .from('circle_invites')
    .select('id, code, created_at, used_by, used_at, expires_at')
    .eq('circle_id', circleId)
    .is('used_by', null)   // only unused codes
    .order('created_at', { ascending: false })

  if (error) throw error
  return data
}

// ─── Join a circle by invite code ────────────────────────────────────────────
export async function joinByCode(code, userId) {
  // 1. Look up the invite
  const { data: invite, error: inviteErr } = await supabase
    .from('circle_invites')
    .select('id, circle_id, used_by, expires_at, circles(name)')
    .eq('code', code.toUpperCase())
    .single()

  if (inviteErr) throw new Error('Invite code not found.')
  if (invite.used_by) throw new Error('This invite has already been used.')
  if (invite.expires_at && new Date(invite.expires_at) < new Date()) {
    throw new Error('This invite has expired.')
  }

  // 2. Add user as member
  const { error: memberErr } = await supabase
    .from('circle_members')
    .insert({ circle_id: invite.circle_id, user_id: userId, role: 'member' })

  if (memberErr) {
    if (memberErr.code === '23505') throw new Error("You're already in this circle!")
    throw memberErr
  }

  // 3. Mark invite as used
  await supabase
    .from('circle_invites')
    .update({ used_by: userId, used_at: new Date().toISOString() })
    .eq('id', invite.id)

  return invite.circles
}